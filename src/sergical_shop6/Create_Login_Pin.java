    private void CreateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CreateMouseClicked

        if(user.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "User Id Is Empty");
            user.requestFocus();
        }else{
            try {
                contest();
                String s="INSERT INTO `pharmacy_management`.`user_id` (`id`,`status`) VALUES (?,?)";
                ps=ct.prepareCall(s);
                
                ps.setString(1,user.getText());
                   ps.setString(2,jComboBox1.getSelectedItem().toString());
                int m=ps.executeUpdate();
                if(m>0){
                user.setText("");
                user.requestFocus();
                jComboBox1.setSelectedIndex(0);
                }else{
                JOptionPane.showMessageDialog(rootPane, "Failed.....");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Create_Login_Pin.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }
    }//GEN-LAST:event_CreateMouseClicked

    private void UpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateMouseClicked
           try {
               // TODO add your handling code here:
               if(user.getText().isEmpty()){
                   JOptionPane.showMessageDialog(Create_Panel, "User Id Is Empty");
                   user.requestFocus();
               }else if(user1.getText().isEmpty()){
                   JOptionPane.showMessageDialog(Create_Panel, "Old User Id Is Empty");
                   user1.requestFocus();
               }else{
                   contest();
               String s="UPDATE `pharmacy_management`.`user_id` SET `id` = '"+user.getText()+"' WHERE `id` = '"+user1.getText()+"' AND `status`='"+jComboBox1.getSelectedItem()+"'";
               ps = ct.prepareCall(s);
               int m=ps.executeUpdate();
               if(m>0){
                   JOptionPane.showMessageDialog(rootPane, "User Id Update.....");
                   user.setText("");
                    user1.setText("");
               }else{
                   JOptionPane.showMessageDialog(rootPane, "User Id  Update Failed.....");
               }
               
               
               }
           } catch (SQLException ex) {
               Logger.getLogger(Create_Login_Pin.class.getName()).log(Level.SEVERE, null, ex);
           }
        
    }//GEN-LAST:event_UpdateMouseClicked

